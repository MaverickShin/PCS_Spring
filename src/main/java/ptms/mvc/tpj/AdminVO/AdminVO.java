package ptms.mvc.tpj.AdminVO;

public class AdminVO {

	private String mg_id;
	private String mg_pwd;
	private String mg_kind;
	private String author;
	
	public AdminVO() {}

	public String getMg_id() {
		return mg_id;
	}

	public void setMg_id(String mg_id) {
		this.mg_id = mg_id;
	}

	public String getMg_pwd() {
		return mg_pwd;
	}

	public void setMg_pwd(String mg_pwd) {
		this.mg_pwd = mg_pwd;
	}

	public String getMg_kind() {
		return mg_kind;
	}

	public void setMg_kind(String mg_kind) {
		this.mg_kind = mg_kind;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}
	
}
